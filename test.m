# Структура проекта
- **app/dao/** - логика работы с базой данных
- **app/data/** - база данных sqlite
- **app/ml/models/** - веса модели Yolo
- **app/ml/ocr.py** - обработка изображения Yolo и ocr
- **app/routers/** - API
- **app/schemas/** - схема ответа сервера
- **app/services/** - сравнение с ngram


